package database;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;

/**
 *
 * @author User
 */
@Entity
@Table(name = "SEARCH_HISTORY")
@NamedQueries({
    @NamedQuery(name = "SearchHistory.findAll", query = "SELECT s FROM SearchHistory s"),
    @NamedQuery(name = "SearchHistory.findById", query = "SELECT s FROM SearchHistory s WHERE s.id = :id"),
    @NamedQuery(name = "SearchHistory.findByKeyword", query = "SELECT s FROM SearchHistory s WHERE s.keyword = :keyword")})
public class SearchHistory implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Basic(optional = false)
    @Column(name = "KEYWORD")
    private String keyword;

    public SearchHistory() {
    }

    public SearchHistory(Integer id) {
        this.id = id;
    }

    public SearchHistory(Integer id, String keyword) {
        this.id = id;
        this.keyword = keyword;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof SearchHistory)) {
            return false;
        }
        SearchHistory other = (SearchHistory) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "database.SearchHistory[ id=" + id + " ]";
    }
    
}
